//绘制棋盘
var g_name;
function grid(cxt) {
    // the first point is (30, 30)
    for (var i = 0; i < 19; i++) {
        cxt.beginPath();
        cxt.moveTo(0 + 30, (i + 1) * 30);
        cxt.lineTo(600 - 30, (i + 1) * 30);
        cxt.stroke();
    }
    for (var i = 0; i < 19; i++) {
        cxt.beginPath();
        cxt.moveTo((i + 1) * 30, 0 + 30);
        cxt.lineTo((i + 1) * 30, 600 - 30);
        cxt.stroke();
    }

}

function ninePoints(cxt) {
    var np = new Array(
        [120, 120], [300, 120], [480, 120],
        [120, 300], [300, 300], [480, 300],
        [120, 480], [300, 480], [480, 480]
    );

    for (var i = 0; i < np.length; i++) {
        //circle
        cxt.beginPath();
        cxt.arc(np[i][0], np[i][1], 3, 0, 2 * Math.PI, false);
        cxt.fillStyle = "black";
        cxt.fill();
    }
}


var move_count = 0;
function mousedownHandler(e) {
    var x, y;
    if (e.offsetX || e.offsetX == 0) {
        x = e.offsetX; //- imageView.offsetLeft;
        y = e.offsetY; //- imageView.offsetTop;
    }
    if (x < 30 - 10 || x > 600 - 30 + 10)
        return;
    if (y < 30 - 10 || y > 600 - 30 + 10)
        return;

    var xok = false;
    var yok = false;
    var x_;
    var y_;
    for (var i = 1; i <= 19; i++) {
        if (x > i * 30 - 15 && x < i * 30 + 15) {
            x = i * 30;
            xok = true;
            x_ = i - 1;
        }
        if (y > i * 30 - 15 && y < i * 30 + 15) {
            y = i * 30;
            yok = true;
            y_ = i - 1;
        }
    }
    if (!xok || !yok)
        return;

    //初步棋子检查
    var row = x_;
    var col = y_;
    if (row < 0 || row > 19 || col < 0 || col > 19) {
        alert("棋子越界错误");
        return;
    }
    // 处理已有棋子在此
    var point;
    if (pan.length === 19)//因为回传过来变成一维数组了
        point = pan[row][col];
    else
        point = pan[row * 19 + col];

    if (point !== 0) {
        alert("此处已有棋子！");
        return;
    }

    sendServer(row, col, move_count);
    move_count++;
    $("#step").val("步数：" + move_count);


    var c = document.getElementById("board");
    var cxt = c.getContext("2d");
    cxt.beginPath();
    cxt.arc(x, y, 15, 0, 2 * Math.PI, false);
    if (move_count % 2 == 1)
        cxt.fillStyle = "black";
    else
        cxt.fillStyle = "white";
    cxt.fill();

}

function mousemoveHandler(e) {
    var x, y;
    if (e.offsetX || e.offsetX == 0) {
        x = e.offsetX;//- imageView.offsetLeft;
        y = e.offsetY;//- imageView.offsetTop;
    }
    if (x < 30 - 10 || x > 600 - 30 + 10)
        return;
    if (y < 30 - 10 || y > 600 - 30 + 10)
        return;

    var xok = false;
    var yok = false;
    for (var i = 1; i <= 19; i++) {
        if (x > i * 30 - 15 && x < i * 30 + 15 && !xok) {
            x = i * 30;
            xok = true;
        }
        if (y > i * 30 - 15 && y < i * 30 + 15 && !yok) {
            y = i * 30;
            yok = true;
        }
        if (xok && yok)
            break;
    }
    if (!xok || !yok)
        return;

    var c = document.getElementById("path");
    var cxt = c.getContext("2d");

    // clear the path
    cxt.clearRect(0, 0, 600, 600);

    // put a new Gray stone
    cxt.beginPath();
    cxt.arc(x, y, 15, 0, 2 * Math.PI, false);
    cxt.fillStyle = "gray";
    cxt.fill();

    cxt.beginPath();
    cxt.arc(x, y, 10, 0, 2 * Math.PI, false);
    if (move_count % 2 == 0)
        cxt.fillStyle = "black";
    else
        cxt.fillStyle = "white";
    cxt.fill();
}

function mouseoutHandler(e) {
    var c = document.getElementById("path");
    var cxt = c.getContext("2d");
    cxt.clearRect(0, 0, 600, 600);
}

function initBoard() {
    var c_path = document.getElementById("path");
    c_path.addEventListener('mousedown', mousedownHandler, false);
    c_path.addEventListener('mousemove', mousemoveHandler, false);
    c_path.addEventListener('mouseout', mouseoutHandler, false);

    var c_board = document.getElementById("board");
    var cxt = c_board.getContext("2d");
    cxt.fillStyle = "#f0ad4e";
    cxt.fillRect(0, 0, 600, 600);

    grid(cxt);
    ninePoints(cxt);

    showPan();
}

function showPan() {
    var c = document.getElementById("board");
    var cxt = c.getContext("2d");
    cxt.strokeStyle = "black";

    /* 清空，重新画线等 */
    cxt.clearRect(0, 0, 600, 600);
    cxt.fillStyle = "sandybrown";
    cxt.fillRect(0, 0, 600, 600);
    grid(cxt);
    ninePoints(cxt);
    var temp;
    for (var i = 0; i < 19; i++) {
        for (var j = 0; j < 19; j++) { //画棋子
            temp = pan[i * 19 + j];
            if (temp === 1) { //black
                var rg = cxt.createRadialGradient((i + 1) * 30 - 3, (j + 1) * 30 - 3, 1, (i + 1) * 30 - 4, (j + 1) * 30 - 4, 11);
                rg.addColorStop(1, /*"black"*/ "#202020");
                rg.addColorStop(0, "gray");
                cxt.beginPath();
                cxt.arc((i + 1) * 30, (j + 1) * 30, 15, 0, 2 * Math.PI, false);
                //cxt.fillStyle="black";
                cxt.fillStyle = rg;
                cxt.fill();

            } else if (temp === 2) { //white
                var rg = cxt.createRadialGradient((i + 1) * 30 - 3, (j + 1) * 30 - 3, 1, (i + 1) * 30 - 4, (j + 1) * 30 - 4, 11);
                rg.addColorStop(1, /*"lightgray"*/ "#e0e0e0");
                rg.addColorStop(0, "white");
                cxt.beginPath();
                cxt.arc((i + 1) * 30, (j + 1) * 30, 15, 0, 2 * Math.PI, false);
                //cxt.fillStyle="white";
                cxt.fillStyle = rg;
                cxt.fill();
            } else if (temp === 7) { // fill color
                cxt.beginPath();
                cxt.arc((i + 1) * 30, (j + 1) * 30, 15, 0, 2 * Math.PI, false);
                cxt.fillStyle = "red";
                cxt.fill();
            }
        }
    }
}


function addLoadEvent(func) {
    var oldonload = window.onload;
    if (typeof window.onload != 'function') {
        window.onload = func;
    } else {
        window.onload = function () {
            oldonload();
            func();
            // clock();
        }
    }
}
